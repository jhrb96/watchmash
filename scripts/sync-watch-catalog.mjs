/**
 * Rebuilds lib/generated/watchCatalog.ts and public/watches/w-* from watch images/.
 * Run: npm run sync:watches
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const sourceDir = path.join(root, "watch images");
const publicDir = path.join(root, "public", "watches");
const outFile = path.join(root, "lib", "generated", "watchCatalog.ts");

const ALLOWED = new Set([
  ".png",
  ".jpg",
  ".jpeg",
  ".webp",
  ".avif",
]);

function humanName(filename, ext) {
  let base = filename.slice(0, -ext.length);
  base = base.replace(/_/g, " ").replace(/-/g, " ");
  base = base.replace(/\s+/g, " ").trim();
  if (base.length > 80) base = `${base.slice(0, 77)}…`;
  return base || filename;
}

const names = fs.existsSync(sourceDir)
  ? fs
      .readdirSync(sourceDir, { withFileTypes: true })
      .filter((d) => d.isFile())
      .map((d) => d.name)
      .filter((name) => {
        if (name === ".DS_Store" || name.startsWith(".")) return false;
        const ext = path.extname(name).toLowerCase();
        return ALLOWED.has(ext);
      })
      .sort((a, b) => a.localeCompare(b, undefined, { sensitivity: "base" }))
  : [];

if (names.length < 2) {
  console.error(
    `sync-watch-catalog: need at least 2 images in "${sourceDir}", found ${names.length}`,
  );
  process.exit(1);
}

fs.mkdirSync(publicDir, { recursive: true });
fs.mkdirSync(path.dirname(outFile), { recursive: true });

for (const f of fs.readdirSync(publicDir)) {
  fs.unlinkSync(path.join(publicDir, f));
}

const watches = [];
for (let i = 0; i < names.length; i++) {
  const srcName = names[i];
  const ext = path.extname(srcName).toLowerCase();
  const padded = String(i + 1).padStart(2, "0");
  const destName = `w-${padded}${ext}`;
  fs.copyFileSync(path.join(sourceDir, srcName), path.join(publicDir, destName));
  watches.push({
    id: `w-${padded}`,
    name: humanName(srcName, path.extname(srcName)),
    image: `/watches/${destName}`,
  });
}

const header = `// Generated by scripts/sync-watch-catalog.mjs — do not edit by hand.
// Source: watch images/  Run: npm run sync:watches

`;

const body = `export type Watch = {
  id: string;
  name: string;
  image: string;
};

export const WATCHES: Watch[] = ${JSON.stringify(watches, null, 2)};
`;

fs.writeFileSync(outFile, header + body, "utf8");
console.log(
  `sync-watch-catalog: wrote ${watches.length} watches → ${path.relative(root, outFile)} and public/watches/`,
);
